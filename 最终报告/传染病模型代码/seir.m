
N  = 383000000;         % 总人口数
ts = [0, 350];         % 求解时间区间
y0 = [N-1, 1 , 0, 0];  % 初始条件[S0 I0 R0]
C = 14;                % 感染的平均持续时间
gamma = 1/C; 
k = 5;                 % 感染者每天平均接触人数
b = 0.04849;           % 接触时的传染概率
beta = k*b;
alpha = 1/10;
[t, y] = ode45(@odeseir, ts,y0, [], beta,gamma,alpha,N);
plot(t,y);
xlabel('时间（天数）'); ylabel('人口数（北美洲）')
legend('易感者', '潜伏者', '隔离（确诊）者', '移除者')








