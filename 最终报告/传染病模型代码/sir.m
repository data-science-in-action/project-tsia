
N  = 40000000;         % 总人口数
ts = [0, 270];         % 求解的时间区间
y0 = [N-1, 1 , 0];     % 初始条件[S0 I0 R0]
C = 14;                % 感染的平均持续时间
gamma = 1/C; 
k = 5;                 % 感染者平均每天接触的人
b = 0.04077;           % 接触时的感染概率
beta = k*b;
d = 0.01;              % 死亡率
[t, y] = ode45(@odesir, ts,y0, [], beta,gamma, N);
y(C+1:end,4) = (N-y(1:end-C,1))*d;
plot(t,y);
xlabel('时间（天数）'); ylabel('人口数（大洋洲）')
legend('易感者', '感染者', '恢复者', '死亡者')
